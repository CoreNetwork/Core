package us.corenetwork.core;

public class ClaimBlocks {

	public int accrued;
	public int bonus;
	public int rank;
	public int remaining;
	public ClaimBlocks(int accrued, int bonus, int rank, int remaining)
	{
		super();
		this.accrued = accrued;
		this.bonus = bonus;
		this.rank = rank;
		this.remaining = remaining;
	}
	
}
